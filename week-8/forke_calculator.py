# add function with two parameters
def add(x, y):
  return x + y

# subtract function with two parameters
def subtract(z, x):
  return z - x

# divide function with two parameters
def divide(z, y):
  return z / y

# print statements to call each function
print(add(1, 2))
print(subtract(4, 1))
print(divide(8, 2))